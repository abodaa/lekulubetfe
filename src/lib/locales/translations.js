export function translate(lang, key, vars) {
  const L = translations[lang] || translations.en;
  let s = L[key] != null ? L[key] : translations.en[key];
  if (s == null) s = key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.split(`{${k}}`).join(String(vars[k]));
    }
  }
  return s;
}
